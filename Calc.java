class Calc {
    public static void main(String[] args) {
        int number1 = 100;
        int number2 = 50;

        // கூட்டல் (Addition)
        int sum = number1 + number2;

        // கழித்தல் (Subtraction)
        int diff = number1 - number2;

        System.out.println("Total Sum is: " + sum);
        System.out.println("Difference is: " + diff);
    }
}